package edu.brown.cs.student.main.Star;

public class Star {
  int starID;
  String properName;
  float datumX;
  float datumY;
  float datumZ;

  public void Star(int starID, String properName, float datumX, float datumY, float datumZ) {
    this.starID = starID;
    this.properName = properName;
    this.datumX = datumX;
    this.datumY = datumY;
    this.datumZ = datumZ;
  }
}
